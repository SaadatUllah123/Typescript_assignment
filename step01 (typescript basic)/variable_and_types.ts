
// variable 
// write the variable difine is three types
// 1.(let) not re-declarable and only updating value

let Course : string = "typescript"; // define variable
course = "javascript"; // updating value 
console.log(course); // accessing value


// 2.(var) re-declarable and updating value

var Course : string = "typescript"; // define variable
course = "javascript"; // updating value 
console.log(course); // accessing value
var Course : string = "typescript"; // re-define variable


// 3.(const) not re-declarable and updating value

const Course : string = "typescript"; // define variable
console.log(course); // accessing value

